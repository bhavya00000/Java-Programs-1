public class Calc {
    public static void main(String args[]) {
        int a = 233, c = -897, d = 252, e = 1, f = 1000;
        System.out.println("Mathematical operation are : \n");
        System.out.println("value of pi: " + Math.PI);
        System.out.println("value of E: " + Math.E);
        System.out.println("value : " + Math.absExact(c));
        System.out.println("value : " + Math.floorDivExact(c, a));
        System.out.println("value : " + Math.log10(f));
        System.out.println("value : " + Math.sqrt(d));
        System.out.println("value : " + Math.sin(e));
        System.out.println("value : " + Math.cos(e));
    }
}